class Employee:  #It Is The Class.
    Language="python"
    Salary=100000

Emp=Employee()
Emp.Name="Apple" #This Is Instance Attribute
print(Emp.Name,Emp.Language,Emp.Salary) #It Is The Object . Here Emp.Name Is Attribute.

Secondemp=Employee()
Secondemp.Name="Mango"
print(Secondemp.Name,Secondemp.Language,Secondemp.Salary)
A="Apple" #String
# The string containing letters are counted from 0 in programming language like A=0 P=1 P=2 L=3 E=4 
AA=A[0:3] #It is used to break down characters
print(AA)
AAA=A[0] #It will print number of chracter in the bracket
print(AAA)

# Negative Slicing
print(A[-4:-1]) #It is same as print(A[1:4]) 
print(A[1:4])
print(A[:3]) #Is same as print(A[0:3]) 
print(A[1:]) #Is same as print(A[1:5]) 

# Slicing with Skip  Values
S="OPERATOR"
print(S[:5:3])


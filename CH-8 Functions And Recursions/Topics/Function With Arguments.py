def F(Name,End): #The (Name) Is Parameter And Also A Variable.We can add multiple var in bracket.
    print("Good Day," + Name) #We Are Puting Variable Or Parameter In Print Func.
    print(End)
    return("OK") #It Will Give the Variable "A" Value Typed In Bracket.

F("Apple","Thanks") #We Are Putting Value In Parameter And Also Calling Func.
A=F("A","B")  #It Will Not Print The A and B It Will Print Ok
print(A)